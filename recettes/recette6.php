<h1 class="font-bold text-2xl text-center my-10">Tarte rustique aux légumes du soleil et feta</h1>
<div class="p-10">
<h1 class="font-bold text-lg">Ingredients :</h1>
<p>
Pâte(s) feuilletée(s) : 1<br>
Poivron(s) : 3<br>
Aubergine(s) : 1<br>
Courgette(s) : 1<br>
Ail : 2 gousse(s)<br>
Échalote(s) : 1<br>
Fromage féta : 100 g<br>
Concentré de tomate<br>
Huile d'olive<br>
Persil ciselé<br>
Sel poivre<br>
</p>
<br><br>
<h1 class="font-bold text-lg">Étapes :</h1>
<p>
Etape 1 : Faîtes revenir environ 15 min l'aubergine, la courgette et les poivrons coupés préalablement en morceaux dans un peu d'huile à feu vif. Ajoutez-y l'ail, l'échalote, le sel et le poivre.<br><br>
Etape 2 : Mettez la pâte feuilletée dans un plat à tarte. Piquez le fond avec une fourchette. Etalez dessus le concentré de tomate.<br><br>
Etape 3 : Déposez les légumes puis la feta émiettée. Parsemez de persil. Repliez les bords de la pâte vers le centre.<br><br>
Etape 4 : Mettez au four pendant environ 45 min, en milieu de cuisson lorsque le dessus est coloré déposez une feuille d'aluminium pour que la cuisson se termine tranquillement sans brûler.<br><br>
</p>
