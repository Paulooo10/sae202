<h1 class="font-bold text-2xl text-center my-10">Crumble aux légumes</h1>
<div class="p-10">
<h1 class="font-bold text-lg">Ingredients :</h1>
<p>
gingembre en poudre<br>
coriandre en poudre<br>
sel<br>
poivre<br>
100 g de beurre tendre<br>
8 carottes moyennes<br>
2 courgettes<br>
4 pommes de terre<br>
125 g de pistaches<br>
125 g de noisettes<br>
40 g de mozzarella<br>
</p>
<br><br>
<h1 class="font-bold text-lg">Étapes :</h1>
<p>
<br><br>
ÉTAPE 1 : Éplucher les légumes et les couper en dés, les faire précuire à la vapeur (10 à 20 minutes suivant le légume).<br>
<br>
ÉTAPE 2 : Les disposer dans un plat beurré.<br>
<br>
ÉTAPE 3 : Broyer les pistaches et les noisettes et mélanger avec le beurre pour obtenir une pâte granulée.<br>
<br>
ÉTAPE 4 : Mettre les légumes cuits à la vapeur dans le plat et ajouter la mozzarella en tranches aux légumes.<br>
<br>
ÉTAPE 5 : Mélanger la coriandre, le gingembre en poudre, saler et répartir le beurre pistache / noisette sur les légumes.<br>
<br>
ÉTAPE 6 : Passer au grill 5 à 7 min et servir chaud.<br>
<br>
</p>
