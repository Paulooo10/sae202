<h1 class="font-bold text-2xl text-center my-10">Gratin aux aubergines et aux tomates au fromage</h1>
<div class="p-10">
<h1 class="font-bold text-lg">Ingredients :</h1>
<p>
Aubergine(s) : 1<br>
Tomate(s) fraîche(s) : 1<br>
Ail : 1.5 gousse(s)<br>
Fromage râpé : 66.8 g<br>
Persil frais : 0.5 bouquet(s)<br>
Basilic frais 0.5 bouquet(s)<br>
Huile d'olive : 0.5 verre(s)<br>
Origan : 0.5 pincée(s)<br>
</p>
<br><br>
<h1 class="font-bold text-lg">Étapes :</h1>
<p>
Etape 1 : Préchauffez votre four à 180ºC. Pelez et hachez l’ail très finement. Nettoyez et épongez le persil ainsi que le basilic puis hachez-les aux ciseaux ou au hachoir électrique.<br><br>
Etape 2 : Versez l'huile d'olive dans un bol. Ajoutez l'origan, l’ail, le persil et le basilic hachés puis salez et poivrez. Mélangez et réservez. La marinade de votre gratin aux aubergines, aux tomates et au fromage est prête.<br><br>
Etape 3 :  Pelez les aubergines et coupez-les en fines rondelles. Saupoudrez de sel pour les dégorger et éviter que celles-ci ne rendent trop d'eau pendant la cuisson. Nettoyez et épongez les tomates. Coupez-les également en rondelles puis placez-les sur du papier absorbant.<br><br>
Etape 4 : Dans un plat à gratin, placez une couche de rondelles d’aubergines, une couche de mélange aux herbes hachées, une couche de rondelles de tomates puis une nouvelle couche de mélange aux herbes hachées. Répétez l'opération jusqu'à épuisement des ingrédients. Terminez par parsemer le gratin de fromage râpé afin de le gratiner parfaitement.<br><br>
Etape 5 : Enfournez et faites chauffer pendant 30 min jusqu'à ce que le fromage soit bien doré.<br><br>
Etape 6 :  À la sortie du four, servez de suite le gratin aux aubergines cuit, aux tomates et au fromage dans les assiettes. Dégustez de suite accompagné d’un morceau de viande ou d'un filet de poisson et/ou une salade verte assaisonnée.<br><br>
</p>
