<div class="p-10">
<h1 class="font-bold text-lg">Ingredients :</h1>
<p>
huile d'olive<br>
2 carottes<br>
500 g de couscous moyen<br>
2 navets<br>
2 artichauts violets<br>
1 grosse poignée de petits pois surgelés<br>
1 poignée de haricots verts surgelés coupés en 3<br>
1 grosse poignée de fèves fraîches écossées<br>
</p>
<br><br>
<h1 class="font-bold text-lg">Étapes :</h1>
<p>
ÉTAPE 1 : Mouiller le couscous dans un grand saladier avec 1/2 l d'eau + 1 cuillère à café de sel + 1 cuillère à soupe d'huile.<br>
<br>
ÉTAPE 2 : Mélanger avec une fourchette, puis remuer régulièrement cette préparation pour détacher les grains.<br>
<br>
ÉTAPE 3 : Nettoyer les légumes. Couper les carottes et navets en dés, les fonds d'artichauts en 8.<br>
<br>
ÉTAPE 4 : Mettre les légumes dans le haut du couscoussier, saler, couvrir et faire cuire à la vapeur 20 min.<br>
<br>
ÉTAPE 5 : Au bout de ces 20 min, rajouter la graine et arroser d'un filet d'huile d'olive. Ne pas couvrir. Compter environ 10 min quand la vapeur passe au travers de la graine.<br>
<br>
ÉTAPE 6 : Reverser dans le saladier et mélanger l'ensemble en incorporant de nouveau un peu d'huile d'olive.<br>
</p>
