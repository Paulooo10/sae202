<div class="p-10">
<h1 class="font-bold text-lg">Ingredients :</h1>
<p>
Courgette(s) : 2<br>
Blanc(s) de poulet : 200 c. à soupe<br>
Tomate(s) séchée(s) : 10<br>
Oignon(s) : 1<br>
Mozzarella : 1 boule<br>
Feuille(s) de basilic<br>
Huile d'olive<br>
Sel poivre<br>
</p>
<br><br>
<h1 class="font-bold text-lg">Étapes :</h1>
<p>
Etape 1 : Nettoyez et coupez les courgettes en deux.<br><br>
Etape 2 : Evidez l'intérieur avec une cuillère.<br><br>
Etape 3 : Pelez et hachez l'oignon.<br><br>
Etape 4 : Taillez les blancs de poulet en morceaux.<br><br>
Etape 5 : Faites chauffer 1 c. à soupe d'huile d'olive dans une poêle.<br><br>
Etape 6 : Faites-y suer l'oignon jusqu'à ce qu'il soit doré.<br><br>
Etape 7 : Ajoutez les blancs de poulet, les tomates séchées émincées, du sel et du poivre.<br><br>
Etape 8 : Laissez cuire 6-7 minutes puis retirez du feu.<br><br>
Etape 9 : Préchauffez le four à 180°C.<br><br>
Etape 10 : Répartissez la préparation au poulet sur les courgettes.<br><br>
Etape 11 : Egouttez et coupez la mozzarella en dès.<br><br>
Etape 12 : Placez-la sur la garniture.<br><br>
Etape 13 : Déposez les courgettes farcies dans un plat à gratin.<br><br>
Etape 14 : Enfournez pendant 40 minutes.<br><br>
Etape 15 : Servez bien chaud accompagné de riz et décoré de feuilles de basilic.<br><br>
</p>
